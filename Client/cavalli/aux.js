function color_eng2it(color){
	var color_it = "";
	switch (color){
		case "red":
			color_it = "Rosso";
			break;
		case "blue":
			color_it = "Blu";
			break;
		case "green":
			color_it = "Verde";
			break;
		case "yellow":
			color_it = "Giallo";
			break;
		case "white":
			color_it = "Bianco";
			break;
	}
	return color_it;
}
